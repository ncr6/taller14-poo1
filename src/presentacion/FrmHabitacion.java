    private void btnRegistrarEstActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarEstActionPerformed
        try {
            Huesped huesp = new Huesped(txtNombre.getText(), txtApellido.getText(), txtTipoDoc.getText(), txtNumDoc.getText(), txtTelf.getText());
            if (!hab.getHuespedes().isEmpty())
                hab.setHuespPos(0, huesp);
            else
                hab.addHuesp(huesp);
            hab.setFechaIngreso(txtFecha.getText());
            hab.setDiasReserva(Integer.parseInt(txtDiasReserva.getText()));
            lblAvisoGuard.setText("Estadía registrada");
            activarCajas(false);
            hab.setDisponible(false);
            actualizarCajas();            
        } catch (Exception ex) {
            System.out.println(ex);
            JOptionPane.showMessageDialog(null, "Por favor rellene todos los campos", "Aviso", 2);
        }
    }//GEN-LAST:event_btnRegistrarEstActionPerformed

