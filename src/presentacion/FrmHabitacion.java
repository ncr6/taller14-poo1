    private void activarCajas(boolean estado){
        txtNombre.setEditable(estado);
        txtApellido.setEditable(estado);
        txtTipoDoc.setEditable(estado);
        txtNumDoc.setEditable(estado);
        txtTelf.setEditable(estado);
        txtFecha.setEditable(estado);
    }

    private void limpiarCajas(){
        txtNombre.setText(null);
        txtApellido.setText(null);
        txtTipoDoc.setText(null);
        txtNumDoc.setText(null);
        txtTelf.setText(null); 
        txtFecha.setText(null); 
    }
    
    private void actualizarCajas(){
        if (hab.isDisponible())
            txtEstado.setText("Disponible");
        else
            txtEstado.setText("Ocupada");
        
        if(!hab.getHuespedes().isEmpty()){
            txtNombre.setText(hab.getHuespedes().get(0).getNombres());
            txtApellido.setText(hab.getHuespedes().get(0).getApellidos());
            txtTipoDoc.setText(hab.getHuespedes().get(0).getTipoDoc());
            txtNumDoc.setText(hab.getHuespedes().get(0).getNumDoc());
            txtTelf.setText(hab.getHuespedes().get(0).getTelf()); 
            txtFecha.setText(hab.getFechaIngreso());    
        } else {
            limpiarCajas();  
        }
    }
    
